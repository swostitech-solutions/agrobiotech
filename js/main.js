(function ($) {
    "use strict";

    // Spinner
    var spinner = function () {
        setTimeout(function () {
            if ($('#spinner').length > 0) {
                $('#spinner').removeClass('show');
            }
        }, 1);
    };
    spinner();
    
    
    // Initiate the wowjs
    new WOW().init();


    // Fixed Navbar
    $(window).scroll(function () {
        if ($(window).width() < 992) {
            if ($(this).scrollTop() > 45) {
                $('.fixed-top').addClass('bg-white shadow');
            } else {
                $('.fixed-top').removeClass('bg-white shadow');
            }
        } else {
            if ($(this).scrollTop() > 45) {
                $('.fixed-top').addClass('bg-white shadow').css('top', -45);
            } else {
                $('.fixed-top').removeClass('bg-white shadow').css('top', 0);
            }
        }
    });
    
    
    // Back to top button
    $(window).scroll(function () {
        if ($(this).scrollTop() > 300) {
            $('.back-to-top').fadeIn('slow');
        } else {
            $('.back-to-top').fadeOut('slow');
        }
    });
    $('.back-to-top').click(function () {
        $('html, body').animate({scrollTop: 0}, 1500, 'easeInOutExpo');
        return false;
    });


    // Testimonials carousel
    $(".testimonial-carousel").owlCarousel({
        autoplay: true,
        smartSpeed: 1000,
        margin: 25,
        loop: true,
        center: true,
        dots: false,
        nav: true,
        navText : [
            '<i class="bi bi-chevron-left"></i>',
            '<i class="bi bi-chevron-right"></i>'
        ],
        responsive: {
            0:{
                items:1
            },
            768:{
                items:2
            },
            992:{
                items:3
            }
        }
    });

    
})(jQuery);



// Number validation
const phoneInput = document.getElementById("phone");
const phoneError = document.getElementById("phone-error");

// Define a regular expression pattern for a valid phone number
const phonePattern = /^\d{10}$/; // Adjust the pattern as needed for your specific requirements

// Function to validate the phone number
function validatePhoneNumber() {
    if (phonePattern.test(phoneInput.value)) {
        phoneError.textContent = ""; // Clear the error message
        phoneInput.setCustomValidity(""); // Clear the built-in validation message
    } else {
        phoneError.textContent = "Please enter a valid 10-digit phone number.";
        phoneInput.setCustomValidity("Please enter a valid 10-digit phone number.");
    }
}

// Add an event listener to validate the phone number when the input changes
phoneInput.addEventListener("input", validatePhoneNumber);


